import fs from 'node:fs';
import path from 'node:path';
const root = process.cwd();
const skillsDir = path.join(root, 'src/lib/templates/skills');
function parseFrontmatter(raw) {
  const m = /^---\s*\r?\n([\s\S]*?)\r?\n---\s*\r?\n?([\s\S]*)$/m.exec(raw);
  const block = m ? m[1] : '';
  const body = (m ? m[2] : raw).trim();
  const fm = {};
  for (const line of block.split(/\r?\n/)) {
    const mm = /^([a-zA-Z_][a-zA-Z0-9_]*)\s*:\s*(.*)$/.exec(line);
    if (!mm) continue;
    const key = mm[1];
    let val = mm[2].trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) val = val.slice(1, -1).replace(/\\"/g, '"');
    if (key === 'featured' || key === 'recommended') {
      const n = Number(val);
      if (Number.isFinite(n)) fm[key] = n;
    } else if (key === 'tags') {
      const arr = /^\[(.*)\]$/.exec(val);
      if (arr) fm.tags = arr[1].split(',').map(s => s.trim().replace(/^["']|["']$/g, '')).filter(Boolean);
    } else {
      fm[key] = val;
    }
  }
  return {fm, body};
}
function meta(id, fm, hasHtml, hasMd) {
  const out = { id, zhName: fm.zh_name || fm.name || id, enName: fm.en_name || id, emoji: fm.emoji || '✨', description: fm.description || '', category: fm.category || 'other', scenario: fm.scenario || 'marketing', aspectHint: fm.aspect_hint || '', tags: Array.isArray(fm.tags) ? fm.tags : [], body: '' };
  if (typeof fm.featured === 'number') out.featured = fm.featured;
  if (typeof fm.recommended === 'number') out.recommended = fm.recommended;
  if (fm.example_id || hasMd || hasHtml) out.example = { id: fm.example_id || `example-${id}`, name: fm.example_name || `${out.zhName} 示例`, format: fm.example_format || 'markdown', tagline: fm.example_tagline || '', desc: fm.example_desc || '', hasHtml, hasMd, ...(fm.example_source_url ? {source: {url: fm.example_source_url, label: fm.example_source_label || fm.example_source_url}} : {}) };
  return out;
}
const skills = [];
for (const id of fs.readdirSync(skillsDir).sort()) {
  const dir = path.join(skillsDir, id);
  if (!fs.statSync(dir).isDirectory()) continue;
  const skillPath = path.join(dir, 'SKILL.md');
  if (!fs.existsSync(skillPath)) continue;
  const {fm, body} = parseFrontmatter(fs.readFileSync(skillPath, 'utf8'));
  const hasHtml = fs.existsSync(path.join(dir, 'example.html'));
  const hasMd = fs.existsSync(path.join(dir, 'example.md'));
  const item = meta(id, fm, hasHtml, hasMd);
  item.body = body;
  item.exampleContent = hasMd ? fs.readFileSync(path.join(dir, 'example.md'), 'utf8') : '';
  item.exampleHtml = hasHtml ? fs.readFileSync(path.join(dir, 'example.html'), 'utf8') : '';
  skills.push(item);
}
fs.writeFileSync(path.join(root, 'src/lib/templates/generated.ts'), `/* Generated by scripts/generate-static-templates.mjs. */\nimport type { StaticTemplate } from './static-types';\n\nexport const STATIC_TEMPLATES = ${JSON.stringify(skills, null, 2)} satisfies StaticTemplate[];\n`);
